import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outDir = path.join(root, "app", "build", "mcp-runtime");
const nodeModulesOut = path.join(outDir, "node_modules");

const packageRoots = [
  path.join(root, "mcp", "package.json"),
  path.join(root, "core", "package.json"),
];

async function readJson(filePath) {
  return JSON.parse(await fs.readFile(filePath, "utf8"));
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function copyDir(from, to) {
  await fs.rm(to, { recursive: true, force: true });
  await fs.mkdir(path.dirname(to), { recursive: true });
  await fs.cp(from, to, {
    recursive: true,
    dereference: true,
    filter: (source) => !source.includes(`${path.sep}.tsbuildinfo`),
  });
}

function packageDir(packageName) {
  return path.join(root, "node_modules", ...packageName.split("/"));
}

async function copyNpmPackage(packageName, seen = new Set()) {
  if (seen.has(packageName)) return;
  seen.add(packageName);

  const sourceDir = packageDir(packageName);
  const packageJsonPath = path.join(sourceDir, "package.json");
  if (!(await pathExists(packageJsonPath))) {
    throw new Error(`Missing dependency package: ${packageName}`);
  }

  await copyDir(sourceDir, path.join(nodeModulesOut, ...packageName.split("/")));
  const packageJson = await readJson(packageJsonPath);
  const deps = Object.keys(packageJson.dependencies ?? {});
  for (const dep of deps) {
    await copyNpmPackage(dep, seen);
  }
}

async function main() {
  await fs.rm(outDir, { recursive: true, force: true });
  await fs.mkdir(outDir, { recursive: true });

  for (const packageJsonPath of packageRoots) {
    const pkg = await readJson(packageJsonPath);
    for (const dep of Object.keys(pkg.dependencies ?? {})) {
      if (!dep.startsWith("@toraseo/")) {
        await copyNpmPackage(dep);
      }
    }
  }

  await copyDir(path.join(root, "mcp", "dist"), path.join(outDir, "mcp", "dist"));
  await fs.copyFile(
    path.join(root, "mcp", "package.json"),
    path.join(outDir, "mcp", "package.json"),
  );

  const coreOut = path.join(nodeModulesOut, "@toraseo", "core");
  await copyDir(path.join(root, "core", "dist"), path.join(coreOut, "dist"));
  await fs.copyFile(path.join(root, "core", "package.json"), path.join(coreOut, "package.json"));

  // core/constants.js reads ../../../mcp/package.json in the monorepo layout.
  // Keep a tiny compatibility package there for the packaged runtime.
  await fs.mkdir(path.join(nodeModulesOut, "mcp"), { recursive: true });
  await fs.copyFile(
    path.join(root, "mcp", "package.json"),
    path.join(nodeModulesOut, "mcp", "package.json"),
  );

  await fs.writeFile(
    path.join(outDir, "README.txt"),
    "ToraSEO packaged MCP runtime. This directory is generated by scripts/prepare-mcp-runtime.mjs.\n",
    "utf8",
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
